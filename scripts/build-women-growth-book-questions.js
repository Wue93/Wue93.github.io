function readConst(source, name, nextName) {
  const startMarker = `const ${name} = `;
  const start = source.indexOf(startMarker);
  const end = source.indexOf(`\n\nconst ${nextName}`, start);
  if (start < 0 || end < 0) throw new Error(`无法读取 ${name}`);
  const literal = source.slice(start + startMarker.length, end).trim().replace(/;$/, "");
  return Function(`return (${literal})`)();
}

function hash(value) {
  return [...value].reduce((total, character) => (total * 31 + character.codePointAt(0)) >>> 0, 0);
}

