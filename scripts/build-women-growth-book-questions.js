function buildQuestions(title, regionId, topicTitle, topicQuestions, focus) {
  if (customQuestions[title]) return customQuestions[title];
  const baseQuestion = topicQuestions[hash(title) % topicQuestions.length];
  return [
    regionFrames[regionId](title, focus),
    `带着《${title}》的视角回到自己的生活：${baseQuestion}`
  ];
}

const source = fs.readFileSync(mapPath, "utf8");
const topicCatalog = readConst(source, "topicCatalog", "topicIssueQuestions");
const topicIssueQuestions = readConst(source, "topicIssueQuestions", "legacyBooks");
const metadata = fs.existsSync(metadataPath)
  ? JSON.parse(fs.readFileSync(metadataPath, "utf8")).books || []
  : [];
const metadataByTitle = new Map(metadata.map((book) => [book.title, book]));
const occurrences = new Map();

const renderedBooks = new Set();
Object.entries(topicCatalog).forEach(([regionId, catalogTopics]) => {
  catalogTopics.forEach((topic, topicIndex) => {
    topic.books.forEach((title) => {
      const item = { regionId, topicTitle: topic.title, topicIndex };
      occurrences.set(title, [...(occurrences.get(title) || []), item]);
    });
  });
});

const books = [...occurrences.entries()].map(([title, locations]) => {
  const primary = locations[0];
  const focus = focusFor(title, primary.topicTitle);
  const questions = buildQuestions(
    title,
    primary.regionId,
    primary.topicTitle,
    topicIssueQuestions[primary.regionId][primary.topicIndex],
    focus
  );
  return {
    title,
    author: metadataByTitle.get(title)?.author || "",
    region: regionNames[primary.regionId],
    topic: primary.topicTitle,
    mapped_topics: locations.map((location) => `${regionNames[location.regionId]} · ${location.topicTitle}`),
    focus,
    questions
  };
});

const allQuestions = books.flatMap((book) => book.questions);
const exactDuplicates = allQuestions.filter((question, index) => allQuestions.indexOf(question) !== index);
const incomplete = books.filter((book) => book.questions.length !== 2 || book.questions.some((question) => !question.trim()));
if (exactDuplicates.length || incomplete.length) {
  throw new Error(`问题校验失败：重复 ${new Set(exactDuplicates).size}，不完整 ${incomplete.length}`);
}

